# Обратен индекс: "българия" -> "BG", "alemania" -> "DE"
REVERSE_LOOKUP: dict = {}
for _iso2, _names in COUNTRY_TRANSLATIONS.items():
    for _lang, _name in _names.items():
        if _name:
            REVERSE_LOOKUP[_name.lower()] = _iso2


def translate_country(iso2: str, lang: str) -> str:
    """Връща името на държавата на избрания език."""
    row = COUNTRY_TRANSLATIONS.get(iso2.upper())
    if not row:
        return iso2
    return row.get(lang) or row.get("en") or iso2


def resolve_iso2_from_text(query: str) -> str:
    """Търси ISO2 код по текст на ВСЕКИ от 5-те езика."""
    q = query.strip().lower()
    if not q:
        return ""
    if q in REVERSE_LOOKUP:
        return REVERSE_LOOKUP[q]
    for name_lower, iso2 in REVERSE_LOOKUP.items():
        if q in name_lower or name_lower in q:
            return iso2
    return ""


